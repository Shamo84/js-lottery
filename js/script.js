document.getElementById('gioca').addEventListener ('click',
  function() {
    houseNumber = Math.floor(Math.random() * 10);
    document.getElementById('il-numero-estratto-e').classList.add("display");
    document.getElementById('il-numero-estratto-e').classList.remove("hidden");
    document.getElementById('numero-estratto').innerHTML=houseNumber;
    document.getElementById('numero-estratto').classList.add("display");
    document.getElementById('numero-estratto').classList.remove("hidden");
    if (houseNumber == userNumber) {
      document.getElementById('congratulazioni').classList.add("display");
      document.getElementById('congratulazioni').classList.remove("hidden");
      document.getElementById('ritenta').classList.add("hidden");
      document.getElementById('ritenta').classList.remove("display");

    } else {
      document.getElementById('congratulazioni').classList.add("hidden");
      document.getElementById('congratulazioni').classList.remove("display");
      document.getElementById('ritenta').classList.add("display");
      document.getElementById('ritenta').classList.remove("hidden");
    }
  }
);
